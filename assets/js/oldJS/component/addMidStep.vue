<template>
  <el-popover popper-class="add-mid-step-popup" placement="right" v-model="isShow" trigger="click">
    <div class="add-step-button" slot="reference">
      <slot></slot>
    </div>
    <div
      class="action-item"
      @click="selectElement(action)" v-if="availableList && availableList.includes('addCategory')">
      Add tag
    </div>
    <div
      class="action-item"
      @click="selectElement(sendText)" v-if="availableList && availableList.includes('sendText')">
      Text
    </div>
    <div
      class="action-item"
      @click="selectElement(sendMedia)" v-if="availableList && availableList.includes('sendMedia')">
      Image
    </div>
    <div
      class="action-item"
      @click="selectElement(delay)" v-if="availableList && availableList.includes('delay')">
      Delay
    </div>
    <add-step-popup
      v-if="false"
      :available-list="availableList"
      @add-step="selectElement($event)"
    >
      <div
        class="action-item">
        Skip add tag
      </div>
    </add-step-popup>

  </el-popover>
</template>

<script>
import actions from '../elements/actions';
import messages from '../elements/messages';
import addStepPopup from './addStepPopup';

export default {
  data() {
    return {
      isShow: false,
    }
  },

  props:['availableList'],

  components: {
    addStepPopup
  },

  computed: {
    action() {
      const addTagAction = actions.find(action => action.template.body.action == 'addCategory');

      return addTagAction.template
    },
    sendText() {
      const sendTextAction = messages.find(action => action.template.body.action == 'sendText');

      return sendTextAction.template
    },
    sendMedia() {
      const sendMediaAction = messages.find(action => action.template.body.action == 'sendMedia');

      return sendMediaAction.template
    },

    delay() {
      const sendDelayAction = messages.find(action => action.template.displaySettings.type == 'delay');

      return sendDelayAction.template
    }
  },

  methods: {
    selectElement(element) {
      this.$emit('add-step', JSON.parse(JSON.stringify(element)));
      this.isShow = false;
    }
  }
}
</script>

